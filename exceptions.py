# -*- coding: utf-8 -*-
"""
异常层级定义（v36 模块化）
⚠️ 本文件定义所有业务异常，禁止跨层抛出实现细节
"""

# ============================================================
# GUI 层异常（gui/）
# ============================================================

class GUIError(Exception):
    """GUI 层基础异常"""
    pass


class UserCancelError(GUIError):
    """用户主动取消操作（如点击取消按钮）"""
    pass


# ============================================================
# 分析层异常（analysis/）
# ============================================================

class AnalysisError(Exception):
    """分析层基础异常"""
    pass


class DataColumnMissingError(AnalysisError):
    """缺少必要的 DataFrame 字段"""
    pass


class InvalidDataError(AnalysisError):
    """数据内容无效（如计划数量为0、空DataFrame）"""
    pass


# ============================================================
# 存储层异常（storage/）
# ============================================================

class StorageError(Exception):
    """存储层基础异常"""
    pass


class DatabaseWriteError(StorageError):
    """数据库写入失败"""
    pass


# ============================================================
# 导出层异常（export/）
# ============================================================

class ExportError(Exception):
    """导出层基础异常"""
    pass


class PPTGenerateError(ExportError):
    """PPT 生成失败"""
    pass


# ============================================================
# 异常使用口诀（给新人背）
# ============================================================
#
# GUI 只抛 GUI 错
# 分析只抛分析错
# 存储只抛存储错
# 业务错，不准吞
# 实现错，不准漏
#
# ============================================================
